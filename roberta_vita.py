def preprocess_text(text, mapping):
    cleaned = text.lower()
    cleaned = cleaned.replace("'", "")
    cleaned = cleaned.replace("...", "")
    for emoji, replacement in mapping.items():
        cleaned = re.sub(re.escape(emoji), replacement, cleaned)
    cleaned = re.sub(r'\s+', ' ', cleaned).strip()
    return cleaned

